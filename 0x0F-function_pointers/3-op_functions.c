#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - adds integers
 * @a: first integer
 * @b: second integer
 *
 * Return: 0 success
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts integers
 * @a: first integer
 * @b: second integer
 *
 * Return: 0 success
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies integers
 * @a: first integer
 * @b: second integer
 *
 * Return: 0 success
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides integers
 * @a: first integer
 * @b: second integer
 *
 * Return: 0 success
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - finds the modulo
 * @a: first integer
 * @b: second integer
 *
 * Return: 0 success
 */
int op_mod(int a, int b)
{
	return (a % b);
}
